let x = 50;
let y = 10;

// 3. Виведення суми
console.log("Сума 50 + 10 =", x + y);

// 4. Оголошення d та множення
let d = x * y;
console.log("Значення d (x * y) =", d);

// 5. Твої дані (можеш змінити на своє реальне прізвище)
let firstName = "Віктор";
let lastName = "Шевченко";
let age = 19;

// 6. Суміщені оператори присвоєння
d /= y;   
x -= d;   
age += x; 

// 7. Виведення результату в одному рядку
console.log(firstName + " " + lastName + " " + age);

// 8. Перевірка на повноліття
if (age >= 18) {
    console.log("студент повнолітній");
} else {
    console.log("студент неповнолітній");
}

// 9. Перевірка рівності
age = "18";
console.log("Результат нестрогої рівності (==):", age == 18);
console.log("Результат строгої рівності (===):", age === 18);

// 10. Цикл for для непарних чисел (Твій варіант N = 16)
let N = 16;
let maxLimit = 10 * N; // 160

console.log(`Непарні числа від 1 до ${maxLimit}:`);
for (let i = 1; i <= maxLimit; i += 2) {
    console.log(i);
}