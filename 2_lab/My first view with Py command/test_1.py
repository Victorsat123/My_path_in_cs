#Операції з Змінними (str)

a = "Привіт"
b = "Світ"

# об'єднання рядків
greeting = a + " " + b
print("Рядок після конкатенації:", greeting)  # Виведе "Привіт Світ"

# Операція повторення рядка
repeat = a * 3
print("Рядок після повторення:", repeat)  # Виведе "ПривітПривітПривіт"
#
#  Використання f-string для форматованого виведення
print(f"Форматований рядок: {a} {b}")  # Виведе "Привіт Світ"



#Операції з цілими числами (int)

# Створення змінної типу int
x = 10
y = 5
# Операція додавання
sum_result = x + y
print("Сума:", sum_result)  # Виведе 15

# Операція множення
multiply_result = x * y
print("Добуток:", multiply_result)  # Виведе 50

# Операція ділення (результат — число з плаваючою точкою)
divide_result = x / y
print("Ділення:", divide_result)  # Виведе 2.0

# Операція ділення націло
floor_division = x // y
print("Ціле ділення:", floor_division)  # Виведе 2

# Операція залишку від ділення
remainder = x % y
print("Залишок від ділення:", remainder)  # Виведе 0



#Операції з числами з плаваючою точкою (float)

# Створення змінної типу float
a_float = 3.145
b_float = 2.531

# Операція додавання
sum_float = a_float + b_float
print("Сума чисел з плаваючою точкою:", sum_float)  # Виведе 5.676

# Операція множення
multiply_float = a_float * b_float
print("Добуток чисел з плаваючою точкою:", multiply_float)  # Виведе 7.959995

# Операція округлення до 2 знаків після коми
rounded = round(a_float, 2)
print("Округлений результат:", rounded)  # Виведе 3.15


# Операції зі списками (list)

# Створення списку
fruits = ["яблуко", "банан", "вишня"]

# Додавання елементів до списку
fruits.append("груша")
print("Список після додавання елемента:", fruits)  # Виведе ["яблуко", "банан", "вишня", "груша"]

# Доступ до елементів списку
first_fruit = fruits[0]
print("Перше фруктове значення:", first_fruit)  # Виведе "яблуко"

# Видалення елемента зі списку
fruits.remove("банан")
print("Список після видалення банану:", fruits)  # Виведе ["яблуко", "вишня", "груша"]

# Операція слайсінгу (отримання підсписку)
sub_fruits = fruits[1:3]
print("Підсписок фруктів:", sub_fruits)  # Виведе ["вишня", "груша"]



# Операції з наборами (set)

# Створення набору
colors = {"червоний", "зелений", "синій"}

# Додавання елемента
colors.add("жовтий")
print("Набір після додавання елемента:", colors)  # Виведе {"червоний", "зелений", "синій", "жовтий"}

# Видалення елемента
colors.discard("зелений")
print("Набір після видалення елемента:", colors)  # Виведе {"червоний", "синій", "жовтий"}

# Перевірка наявності елемента в наборі
is_present = "синій" in colors
print("Чи є синій в наборі?", is_present)  # Виведе True



# Операції зі словниками (dict)

# Створення словника
student = {"name": "Victor", "age": 16, "course": "математика"}

# Доступ до значення по ключу
student_name = student["name"]
print("Ім'я студента:", student_name)  # Виведе "Victor"

# Додавання нового елемента
student["grade"] = "A"
print("Словник після додавання нового елемента:", student)  # Виведе {'name': 'Victor', 'age': 16, 'course': 'математика', 'grade': 'A'}

# Оновлення значення
student["age"] = 21
print("Словник після оновлення віку:", student)  # Виведе {'name': 'Victor', 'age': 21, 'course': 'математика', 'grade': 'A'}

# Видалення елемента
del student["course"]
print("Словник після видалення курсу:", student)  # Виведе {'name': 'Victor', 'age': 21, 'grade': 'A'}
