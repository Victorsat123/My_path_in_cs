# Приклад 1: Цикл for із використанням range() та індексів
letters = ["a", "b", "c"]
print("Приклад 1: Цикл for із використанням range() та індексів:")
for i in range(len(letters)):
    print(f"На позиції {i} знаходиться буква {letters[i]}")
else:
    print("Ця конструкція безглузда!")

# Додаємо порожній рядок для розділення
print("\n" + "="*40 + "\n")

# Приклад 2: Цикл for з ітерацією по елементах списку
fruits = ["яблуко", "банан", "вишня"]
print("Приклад 2: Цикл for з ітерацією по елементах списку:")
for fruit in fruits:
    print(f"Я люблю їсти {fruit}!")

# Додаємо порожній рядок для розділення
print("\n" + "="*40 + "\n")

# Приклад 3: Цикл while
count = 0
print("Приклад 3: Цикл while:")
while count < 3:
    print(f"Цей цикл виконується {count + 1} раз(и).")
    count += 1
else:
    print("Цикл завершився, коли count досяг 3.")
